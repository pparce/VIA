package cu.uno.via.database.modelos;

public class NotasModelo {

    int id = 0;
    String descripcion = "";

    public NotasModelo() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id){
        this.id = id;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion){
        this.descripcion = descripcion;
    }

}

